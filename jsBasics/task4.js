// task 4.1
const PI = Math.PI;
const radiusRound = 5;

const squareRound = (PI * radiusRound) ^ 2;
console.log(`The square of round is ${squareRound.toFixed(2)}`);

// task 4.2
const lengthRectangle = 12;
const widthRectangle = 8;

const squareRectangle = lengthRectangle * widthRectangle;
console.log(`The square of rectangle is ${squareRectangle.toFixed(2)}`);

// task 4.3
const radiusCylinder = 25;
const heightCylinder = 33;

const volumeCylinder = (PI * radiusCylinder) ^ (2 * heightCylinder);
console.log(`The volume of cylinder is ${volumeCylinder.toFixed(2)}`);
